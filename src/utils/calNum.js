export function addTwoNum(augend, addend) {
  const result = augend + addend;
  return result;
}

export function subTwoNum(minuend, subend) {
  const result = minuend - subend;
  return result;
}

export function mulTwoNum(multiplicand, multiplier) {
  const result = multiplicand * multiplier;
  return result;
}

export function divTwoNum(dividend, divisor) {
  const result = dividend / divisor;
  return result;
}
